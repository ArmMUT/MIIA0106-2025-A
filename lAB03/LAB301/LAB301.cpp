#include <iostream>
using namespace std;

int main() {
	int a, b;
	cout << "ENTER a: ";
	cin >> a;
	cout << "ENTER b: ";
	cin >> b;

	cout << "A+B=" << a + b << endl;
	cout << "A-B=" << a - b << endl;
	cout << "A*B=" << a * b << endl;
	cout << "A/B=" << a / b << endl;
	cout << "A%%B=" << a % b << endl;
	return 0;
}